                                        The original mission of the PBX was to provide shared access to limited
                                        resources. Rather than having a separate phone line for each phone, a business
                                        could share a small pool of lines across a much larger pool of stations. When a
                                        call came it was answered by an operator who then connected it with the
                                        appropriate person or department. When someone inside needed to make a call, the
                                        operator connected them with an available line. Frequently these early systems
                                        were simply called “switchboards”.

                                        Over time, operators were replaced by electromechanical and later electronic
                                        systems for managing access to lines. Additional features were added to
                                        automatically route incoming calls, to allow active calls to be transferred
                                        between stations and to permit or deny calls based on various rules. Adjunct
                                        systems were added for voice messaging, call queuing and other value added
                                        services.

                                        Today, a business phone system is much more than just a simple switch. Adjunct
                                        technologies like automated attendant, voice messaging, call queuing and
                                        multi-party conferencing have become standard features. Basic analog and
                                        proprietary digital phones are giving way to standards-based IP phones. Outside
                                        connectivity is now available over the Internet in the form of SIP trunks or
                                        other VoIP services.



